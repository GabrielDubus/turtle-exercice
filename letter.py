def path_I(turtle):
    turtle.pendown()
    turtle.setheading(0)
    turtle.forward(25)
    turtle.backward(12)
    turtle.right(90)
    turtle.forward(50)
    turtle.right(90)
    turtle.forward(12)
    turtle.right(180)
    turtle.forward(25)
    turtle.penup()

def path_L(turtle):
    turtle.pendown()
    turtle.setheading(270)
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(25)
    turtle.penup()

